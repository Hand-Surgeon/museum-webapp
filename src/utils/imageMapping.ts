// 제목으로 이미지 경로 가져오기
export function getImagePath(title: string): string {
  // 정확한 매칭
  if (imageMapping[title]) {
    return imageMapping[title];
  }
  
  // 부분 매칭 시도
  const keys = Object.keys(imageMapping);
  for (const key of keys) {
    if (key.includes(title) || title.includes(key)) {
      return imageMapping[key];
    }
  }
  
  // 매칭 실패시 null 반환
  return '';
}